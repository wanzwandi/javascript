function testingswitch(){
  var nilai = 10;
  switch(nilai){
    case 10:
    console.log("Sangat baik");
    break;
    case 9:
    console.log("Baik");
    break;
    case 8:
    console.log("Cukup baik");
    case 7:
    console.log("Cukup");
    break;
    default:
    console.log("Lainnya");
  }
}
testingswitch()
